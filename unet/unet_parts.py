class PoolingFeatures(nn.Module):
    """
    根据 LessNet 论文：实现三路径参数无关池化（Max, Avg, Min） [cite: 98, 191]
    """
    def __init__(self, kernel_size, stride):
        super().__init__()
        self.pool_max = nn.MaxPool2d(kernel_size=kernel_size, stride=stride)
        self.pool_avg = nn.AvgPool2d(kernel_size=kernel_size, stride=stride)

    def forward(self, x):
        # 论文提到使用 max, average, min 三种池化 [cite: 98, 194]
        # Min Pooling 可以通过对取负后的 Max Pooling 实现
        f_max = self.pool_max(x)
        f_avg = self.pool_avg(x)
        f_min = -self.pool_max(-x) 
        
        # 拼接后的通道数为 输入通道数 * 3 
        return torch.cat([f_max, f_avg, f_min], dim=1)


# 保留原项目中的 Up 块，但需要注意通道数适配
class Up(nn.Module):
    """Upscaling then double conv"""
    def __init__(self, in_channels, out_channels, bilinear=True):
        super().__init__()
        if bilinear:
            self.up = nn.Upsample(scale_factor=2, mode='bilinear', align_corners=True)
            self.conv = DoubleConv(in_channels, out_channels, in_channels // 2)
        else:
            self.up = nn.ConvTranspose2d(in_channels, in_channels // 2, kernel_size=2, stride=2)
            self.conv = DoubleConv(in_channels, out_channels)

    def forward(self, x1, x2):
        x1 = self.up(x1)
        # 这里的 x2 不再是 Encoder 特征，而是对应尺度的池化特征 [cite: 199, 234]
        x = torch.cat([x2, x1], dim=1)
        return self.conv(x)
        

class OutConv(nn.Module):
    def __init__(self, in_channels, out_channels):
        super(OutConv, self).__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=1)

    def forward(self, x):
        return self.conv(x)
